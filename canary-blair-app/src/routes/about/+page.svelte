		</div>
	</section>

	<section class="section">
		<h2>Badges</h2>
		<p>
			Badges are earned over a legislator's entire career — not just one session.
			They reflect patterns in how someone votes, not one-off decisions.
		</p>
		<div class="badge-list">
			<div class="badge-item">
				<span class="badge-icon">💧</span>
				<div>
					<strong>Water Protector</strong>
					<p>Consistently votes for water and environment protections, and against bills that weaken them. You have to show up for the good bills <em>and</em> stand against the bad ones.</p>
				</div>
			</div>
			<div class="badge-item">
				<span class="badge-icon">👷</span>
				<div>
					<strong>Friend of the Worker</strong>
					<p>Consistently votes for worker protections, and against bills that weaken them. Same deal — supporting workers means opposing the bills that hurt them, not just voting for easy wins.</p>
				</div>
			</div>
			<div class="badge-item">
				<span class="badge-icon">🌞</span>
				<div>
					<strong>Renewables Champion</strong>
					<p>Consistently votes for clean and renewable energy — solar, wind, efficiency, storage — and against bills that prop up fossil fuels at its expense. "Energy" isn't automatically bad: we judge who a bill actually benefits, and reward the legislators building the future instead of protecting the past.</p>
				</div>
			</div>
			<div class="badge-item">
				<span class="badge-icon">🦅</span>
				<div>
					<strong>Lone Canary</strong>
					<p>Voted against their own party on bills that help the people. Takes courage to break ranks when it matters.</p>
				</div>
			</div>
			<div class="badge-item">
				<span class="badge-icon">💰</span>
				<div>
					<strong>Never Met a Corporation They Didn't Like</strong>
