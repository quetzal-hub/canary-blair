<div class="container">
	<h1>About Canary Blair</h1>

	<section class="section">
		<h2>Our Mission</h2>
		<p>
			Canary Blair exists to make West Virginia's government transparent to its people.
			We track every bill, every vote, and every legislator — and translate it all into
			plain language that anyone can understand.
		</p>
		<p>
			No political science degree required. No legal jargon. Just the truth about
			what your elected officials are doing with your state.
		</p>
	</section>

	<section class="section">
		<h2>Why "Canary Blair"?</h2>

		<p>
			The name comes from two pillars of West Virginia history.
		</p>

		<figure class="blair-figure">
			<img src="/images/Canary_coal_mine.jpg" alt="A canary in a cage used by coal miners to detect toxic gases underground" class="blair-img" />
			<figcaption>
				The canary went in first. If it stopped singing, you ran.
			</figcaption>
		</figure>

		<p>
			<strong>The canary in the coal mine</strong> — for generations, coal miners carried
			canaries into the tunnels. If the canary stopped singing, it meant danger. The canary
			was an early warning system, and it cost the canary its life. West Virginia's workers
			have always been the canary.
		</p>

		<!-- BLAIR MOUNTAIN HERO IMAGE -->
		<figure class="blair-figure">
			<img src="/images/blair-mountain-hero.jpg" alt="Two coal miners with a machine gun in the brush during the Battle of Blair Mountain, 1921" class="blair-img" />
			<figcaption>
				Ten thousand West Virginia miners marched on Blair Mountain in 1921 — the largest
				labor uprising in American history.
			</figcaption>
		</figure>

		<p>
			<strong>Blair Mountain</strong> — in 1921, ten thousand West Virginia coal miners
			marched on Blair Mountain in the largest labor uprising in American history. They
			fought for the right to organize, for safe working conditions, for basic dignity.
			The government sent planes to bomb them. The companies hired private armies.
			The miners fought anyway.
		</p>

		<!-- SECOND IMAGE — miners, families, or the aftermath -->
		<figure class="blair-figure">
			<img src="/images/blair-mountain-miners.jpg" alt="A West Virginia coal miner with a rifle over his shoulder during the Battle of Blair Mountain, 1921" class="blair-img" />
			<figcaption>
				They carried rifles, wore red bandanas, and called each other "redneck" with pride.
				The word meant something different before it was stolen.
			</figcaption>
		</figure>

		<p>
			Canary Blair carries that spirit. Still watching. Still singing. Still fighting
			for the people who built this state with their hands.
		</p>
	</section>

	<section class="section">
		<h2>How It Works</h2>
		<p>
			All legislative data comes from <strong>LegiScan</strong>, a nonpartisan service
			that tracks state legislation across the United States. We sync WV data daily.
		</p>
		<p>
			Every bill is summarized in plain English — what it does, who benefits, and who
			gets hurt. No jargon, no spin.
		</p>
		<p>
			Vote records are permanent. Nothing is ever deleted. Every vote your legislator
			casts is recorded here forever.
		</p>
	</section>

	<section class="section">
		<h2>Canary Score</h2>
		<p>
			Every legislator receives a Canary Score from 0 to 100 based on two things:
		</p>
		<ol class="score-factors">
			<li>
				<strong>How they vote</strong> — Voting for people-first bills raises the score.
				Voting for corporate-first bills lowers it.
			</li>
			<li>
				<strong>What they sponsor</strong> — Putting your name on a bill is a stronger
				signal than just voting on it. Sponsorship counts more because it shows what a
				legislator is actively working toward — not just going along with the crowd.
				Primary sponsors are weighted more heavily than cosponsors.
			</li>
		</ol>
		<p>
			Both are weighted by impact — a landmark bill that affects thousands of lives
			counts far more than a ceremonial resolution.
		</p>
		<p>
			Votes show what you'll tolerate. Sponsorship shows what you believe in.
		</p>
		<p>
			Based on their score, each legislator is placed into one of six tiers:
		</p>
		<div class="tier-list">
			<div class="tier-item">
				<span class="tier-icon">✨</span>
				<div>
					<strong>Mountaineer</strong> <span class="tier-range">80–100</span>
					<p>Votes like they actually live here.</p>
				</div>
			</div>
			<div class="tier-item">
				<span class="tier-icon">🌱</span>
				<div>
					<strong>Friend of the Holler</strong> <span class="tier-range">60–79</span>
					<p>Not perfect, but they're trying.</p>
				</div>
			</div>
			<div class="tier-item">
				<span class="tier-icon">🌫️</span>
				<div>
					<strong>Weathervane</strong> <span class="tier-range">45–59</span>
					<p>Blows whichever way the lobby goes.</p>
				</div>
			</div>
			<div class="tier-item">
				<span class="tier-icon">🪨</span>
				<div>
					<strong>Company Man</strong> <span class="tier-range">35–44</span>
					<p>Reliable — just not for you.</p>
